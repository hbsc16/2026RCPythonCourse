selection = input("Please choose a treasure box: a, b, or c:\n")
if (selection == "a"):
    print("You just won a day pass to Washington State Parks!")
elif (selection == "b"):
    print("You just won two tickets to a Taylor Swift concert!")
elif (selection == "c"):
    print("You just won a pass to the sunflower festival!")
else:
    print("Sorry, I didn't understand that.")